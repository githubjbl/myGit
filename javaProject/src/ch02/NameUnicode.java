package ch02;

public class NameUnicode {

	public static void main(String[] args) {
		char aValue = '\uc774'; 
		char bValue = '\uc7ac';
		char cValue = '\ubcf5';
		
		System.out.println("char형(\'\\uc774\') : " + aValue);
		System.out.println("char형(\'\\uc7ac\') : " + bValue);
		System.out.println("char형(\'\\ubcf5\') : " + cValue);
		
	}

}
